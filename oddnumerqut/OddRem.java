package oddnumerqut;

public class OddRem {
	
public static void main(String[] args) {
    int a []={21,31,35,37,42};
    int b = 2;
    for (int i = 0; i < a.length; i++) {
    	int quo = a[i]/b;
    	System.out.println("the quotient is-" + quo);
    	for (int j = 0; j <1; j++) {
    		int rem = a[j]%b;
    		System.out.println("the reminder is:"+  +rem);
			
		}
    	}
    
   
		
	}
    
	}


